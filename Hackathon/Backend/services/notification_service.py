def send_alert_auto(recipient_email, ward_data):
    """
    Automated trigger for flood alerts using Gmail API and Llama-powered content.
    """
    service = get_gmail_service()
    if not service:
        return False

    try:
        # Get sender identity
        user_profile = service.users().getProfile(userId="me").execute()
        sender_email = user_profile["emailAddress"]

        # 1. Generate Llama Report
        html_report = generate_flood_report_llama(ward_data)
        
        # 2. Generate PDF Report
        pdf_path = create_flood_report_pdf(ward_data, html_report)
        
        # 3. Prepare Multi-part message
        message = MIMEMultipart()
        message["to"] = recipient_email
        message["from"] = sender_email
        message["subject"] = f"🚨 ALERT: Critical Flood Risk in {ward_data.get('WardName')}"
        
        # Body
        message.attach(MIMEText(html_report, "html"))
        
        # Attachment
        with open(pdf_path, "rb") as attachment:
            part = MIMEBase("application", "octet-stream")
            part.set_payload(attachment.read())
            encoders.encode_base64(part)
            part.add_header(
                "Content-Disposition",
                f"attachment; filename=Flood_Report_{ward_data.get('WardName')}.pdf",
            )
            message.attach(part)
        
        raw = base64.urlsafe_b64encode(message.as_bytes()).decode()
        sent = service.users().messages().send(userId="me", body={"raw": raw}).execute()
        
        # Cleanup PDF
        if os.path.exists(pdf_path):
            os.remove(pdf_path)
            
        logger.info(f"Llama Auto-Alert with PDF sent via Gmail API: {sent['id']}")
        return True

    except Exception as e:
        logger.error(f"Auto-Alert failed: {e}")
        return False