def get_gmail_service():
    if not os.path.exists(TOKEN_FILE):
        logger.error("token.json not found. User must visit /authorize first.")
        return None

    creds = Credentials.from_authorized_user_file(TOKEN_FILE, SCOPES)

    # ✅ FIX: Auto refresh token
    if creds and creds.expired and creds.refresh_token:
        try:
            creds.refresh(Request())
            with open(TOKEN_FILE, "w") as token:
                token.write(creds.to_json())
        except Exception as e:
            logger.error(f"Token refresh failed: {e}")
            return None

    return build("gmail", "v1", credentials=creds)


def generate_flood_report_llama(ward_data):
    """
    Uses Groq Llama to generate a professional, urgent flood report.
    """
    llm = ChatGroq(
        api_key=os.getenv("LLAMA_API_KEY"),
        model=os.getenv("LLAMA_MODEL", "llama3-70b-8192")
    )
    
    ward_name = ward_data.get('WardName', 'Unknown')
    prob = ward_data.get('flood_probability', 0) * 100
    rain = ward_data.get('rainfall_mm', 0)
    elev = ward_data.get('elevation_m', 0)
    drain = ward_data.get('drainage_density', 0)
    urban = ward_data.get('impermeable_ratio', 0) * 100

    prompt = f"""
    You are an Emergency Response System AI. 
    Generate a highly professional, urgent HTML email report for the area of {ward_name}.
    Current Stats:
    - Flood Probability: {prob:.1f}%
    - Rainfall: {rain}mm
    - Elevation: {elev:.1f}m
    - Urban Density: {urban:.1f}%
    - Drainage Index: {drain:.3f}

    Include:
    1. A clear 'CRITICAL' heading.
    2. A brief analysis of why this area is at risk (mention how elevation and drainage density contribute).
    3. A 3-step action plan for local authorities.
    4. Keep it concise, using standard HTML tags.
    """
    
    try:
        response = llm.invoke(prompt)
        return response.content
    except Exception as e:
        logger.error(f"Llama Generation failed: {e}")
        return f"<h1>URGENT ALERT: {ward_name}</h1><p>Risk: {prob:.1f}%</p>"

